# simple assignment
sale_value = 80

# assignment with addition
sale_value += 30
print(sale_value)

# assignment with subtraction
sale_value -= 90
print(sale_value)

# attribution with multiplication
sale_value *= 3
print(sale_value)

# assignment with division
sale_value //= 10
print(sale_value)

# attribution with exponentiation
sale_value **= 2
print(sale_value)

# attribution with module
sale_value %= 6
print(sale_value)
